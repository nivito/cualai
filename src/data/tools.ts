// ── Helper functions ──────────────────────────────────────────

export function getCategoryBySlug(slug: string): Category | undefined {
  return categories.find((c) => c.slug === slug)
}

export function getToolBySlug(slug: string): Tool | undefined {
  return tools.find((t) => t.slug === slug)
}

export function getToolsByCategory(categorySlug: string): Tool[] {
  return tools.filter((t) => t.categories.includes(categorySlug))
}

export function getFeaturedTools(): Tool[] {
  return tools.filter((t) => t.featured)
}

export function getToolCount(): number {
  return tools.length
}

export function getToolCountByCategory(categorySlug: string): number {
  return tools.filter((t) => t.categories.includes(categorySlug)).length
}

export function searchTools(query: string): Tool[] {
  if (!query.trim()) return []

  const q = query.toLowerCase().trim()
  const terms = q.split(/\s+/)

  const scored = tools.map((tool) => {
    let score = 0
    const name = tool.name.toLowerCase()
    const desc = tool.description.toLowerCase()
    const tags = tool.tags.join(" ").toLowerCase()
    const useCases = tool.useCases.join(" ").toLowerCase()
    const cats = tool.categories.join(" ").toLowerCase()

    for (const term of terms) {
      if (name === q) score += 100
      else if (name.includes(term)) score += 50
      if (tags.includes(term)) score += 20
      if (useCases.includes(term)) score += 15
      if (desc.includes(term)) score += 10
      if (cats.includes(term)) score += 5
    }

    return { tool, score }
  })

  return scored
    .filter((s) => s.score > 0)
    .sort((a, b) => b.score - a.score)
    .map((s) => s.tool)
}

export function getSimilarTools(tool: Tool, limit = 4): Tool[] {
  const scored = tools
    .filter((t) => t.id !== tool.id)
    .map((t) => {
      let score = 0
      for (const cat of tool.categories) {
        if (t.categories.includes(cat)) score += 10
      }
      for (const tag of tool.tags) {
        if (t.tags.includes(tag)) score += 3
      }
      if (t.pricing === tool.pricing) score += 2
      return { tool: t, score }
    })
    .filter((s) => s.score > 0)
    .sort((a, b) => b.score - a.score)

  return scored.slice(0, limit).map((s) => s.tool)
}
